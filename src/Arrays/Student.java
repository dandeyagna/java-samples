package Arrays;
public class Student
{
	private String name;
	private int rollNumber;
	
	public Student(String Name, int RollNumber)
	{
		this.name = Name;
		this.rollNumber = RollNumber;
	}
	public String getName()
	{
		return name;
	}
	public int getRollNumber()
	{
		return rollNumber;
	}
}	